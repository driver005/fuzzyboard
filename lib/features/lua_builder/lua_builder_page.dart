    final theme = Theme.of(context);
    final rows = [
      ('==', 'Equal'),
      ('~=', 'Not equal'),
      ('>', 'Greater than'),
      ('<', 'Less than'),
      ('and', 'Logical AND'),
      ('or', 'Logical OR'),
      ('not', 'Logical NOT'),
    ];

    return Wrap(
      spacing: 12,
      runSpacing: 6,
      children: rows.map((r) {
        return Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            Container(
              padding: const EdgeInsets.symmetric(horizontal: 6, vertical: 2),
              decoration: BoxDecoration(
                color: const Color(0xFF8B5CF6).withOpacity(0.1),
                borderRadius: BorderRadius.circular(5),
              ),
              child: Text(r.$1,
                  style: AppTypography.mono
                      .copyWith(fontSize: 11, color: const Color(0xFF8B5CF6))),
            ),
            const SizedBox(width: 4),
            Text(r.$2, style: theme.textTheme.bodySmall),
          ],
        );
      }).toList(),
    );
  }
}

class _SmallToggle extends StatelessWidget {
  final String label;
  final bool active;
  final VoidCallback onTap;

  const _SmallToggle(
      {required this.label, required this.active, required this.onTap});

  @override
  Widget build(BuildContext context) {
    final cs = Theme.of(context).colorScheme;
    return GestureDetector(
      onTap: onTap,
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 150),
        padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 4),
        decoration: BoxDecoration(
          color: active
              ? cs.primary.withOpacity(0.2)
              : cs.onSurface.withOpacity(0.06),
          borderRadius: BorderRadius.circular(6),
        ),
        child: Text(
          label,
          style: TextStyle(
            fontSize: 11,
            fontWeight: FontWeight.w600,
            color: active ? cs.primary : cs.onSurface.withOpacity(0.5),
          ),
        ),
      ),
    );
  }
}

// ── Condition Row ─────────────────────────────────────────────────────────────

class _ConditionRow extends StatefulWidget {
  final _ConditionNode node;
  final _GroupNode parent;
  final VoidCallback onChanged;

  const _ConditionRow({
    required this.node,
    required this.parent,
    required this.onChanged,
  });

  @override
  State<_ConditionRow> createState() => _ConditionRowState();
}

class _ConditionRowState extends State<_ConditionRow> {
  late TextEditingController leftController;
  late TextEditingController rightController;

  @override
  void initState() {
    super.initState();
    leftController = TextEditingController(text: widget.node.left);
    rightController = TextEditingController(text: widget.node.right);
  }

  @override
  void dispose() {
    leftController.dispose();
    rightController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    const ops = ['==', '~=', '>', '<', '>=', '<='];
    final cs = Theme.of(context).colorScheme;

    return Container(
      padding: const EdgeInsets.all(10),
      decoration: BoxDecoration(
        color: cs.surface,
        borderRadius: BorderRadius.circular(10),
        border: Border.all(color: cs.outline.withOpacity(0.2)),
      ),
      child: Row(
        children: [
          _SmallToggle(
            label: 'NOT',
            active: widget.node.negated,
            onTap: () {
              widget.node.negated = !widget.node.negated;
              widget.onChanged();
            },
          ),
          const SizedBox(width: 8),
          Expanded(
            child: TextField(
              decoration: InputDecoration(
                hintText: 'variable',
                contentPadding: const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
                border: OutlineInputBorder(borderRadius: BorderRadius.circular(8), borderSide: BorderSide.none),
                filled: true,
              ),
              controller: leftController,
              onChanged: (v) {
                widget.node.left = v;
                widget.onChanged();
              },
              style: AppTypography.mono.copyWith(fontSize: 12),
            ),
          ),
          const SizedBox(width: 6),
          SizedBox(
            width: 70,
            child: DropdownButtonHideUnderline(
              child: DropdownButton<String>(
                value: widget.node.op,
                items: ops.map((o) => DropdownMenuItem(value: o, child: Text(o))).toList(),
                onChanged: (v) {
                  widget.node.op = v ?? widget.node.op;
                  widget.onChanged();
                },
              ),
            ),
          ),
          const SizedBox(width: 6),
          Expanded(
            child: TextField(
              decoration: InputDecoration(
                hintText: 'value',
                contentPadding: const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
                border: OutlineInputBorder(borderRadius: BorderRadius.circular(8), borderSide: BorderSide.none),
                filled: true,
              ),
              controller: rightController,
              onChanged: (v) {
                widget.node.right = v;
                widget.onChanged();
              },
              style: AppTypography.mono.copyWith(fontSize: 12),
            ),
          ),
          const SizedBox(width: 6),
          _SmallToggle(
            label: '"str"',
            active: widget.node.quoted,
            onTap: () {
              widget.node.quoted = !widget.node.quoted;
              widget.onChanged();
            },
          ),
          const SizedBox(width: 4),
          IconButton(
            icon: Icon(Icons.close, size: 16, color: cs.onSurface.withOpacity(0.4)),
            visualDensity: VisualDensity.compact,
            onPressed: () {
              widget.parent.children.remove(widget.node);
              widget.onChanged();
            },
          ),
        ],
      ),
    );
  }
}

// ── Expression tree nodes ─────────────────────────────────────────────────────

abstract class _ExpressionNode {}

class _GroupNode extends _ExpressionNode {
  String op;
  bool negated;
  final List<_ExpressionNode> children;

  _GroupNode({
    required this.op,
    this.negated = false,
    List<_ExpressionNode>? children,
  }) : children = children ?? [];
}

class _ConditionNode extends _ExpressionNode {
  String left;
  String op;
  String right;
  bool quoted;
  bool negated;

  _ConditionNode({
    this.left = '',
    this.op = '==',
    this.right = '',
    this.quoted = false,
    this.negated = false,
  });
}
